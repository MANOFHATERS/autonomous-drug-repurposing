def normalize_inchikey(raw: Union[str, bytes, None]) -> str | None:
    """Strip whitespace and uppercase an InChIKey, WITHOUT validating format.

    (ARCH-6) — pure normalization, no pattern check.

    Parameters
    ----------
    raw : str, bytes, or None
        The raw InChIKey string.

    Returns
    -------
    str or None
        The cleaned string (stripped + uppercased), or None if input is
        None / empty / not a string.

    Examples
    --------
    >>> normalize_inchikey("  bsnrymutxbxsq-uhfffaoySA-N  ")
    'BSYNRYMUTXBXSQ-UHFFFAOYSA-N'
    """
    if raw is None:
        return None
    # [TEST-17] Accept bytes (decode with UTF-8).
    if isinstance(raw, bytes):
        try:
            raw = raw.decode("utf-8", errors="strict")
            # v40 ROOT FIX (P1 #33): corrected the log message. This
            # code is inside ``normalize_inchikey`` (not
            # ``standardize_inchikey``). The previous message
            # misattributed the decode to the wrong function, misleading
            # operators during debugging.
            logger.info(
                "normalize_inchikey: decoded bytes input to str"
            )
        except UnicodeDecodeError:
            return None
    if not isinstance(raw, str):
        logger.debug(
            "normalize_inchikey: received non-string input of type %s",
            type(raw).__name__,
        )
        return None
    cleaned = raw.strip()
    if not cleaned:
        return None
    # [CODE-25] Uppercase the input.
    return cleaned.upper()


def validate_inchikey(
    key: str,
    *,
    strict: bool = False,
) -> bool:
    """Validate an InChIKey string WITHOUT normalizing it (ARCH-6).

    Parameters
    ----------
    key : str
        The InChIKey to validate.  Must already be normalized (strip+upper).
    strict : bool
        If True, require the version char to be S or N (SCI-8).
        If False (default), accept any uppercase letter as the version
        char (matches DB layer's loose contract).

    Returns
    -------
    bool
        True if valid, False otherwise.
    """
    if not isinstance(key, str) or not key:
        return False
    # [ARCH-1] Standard 27-char pattern.
    if _INCHIKEY_PATTERN.match(key):
        if strict:
            # [SCI-8] Strict mode: last char must be S or N.
            vc = _validate_inchikey_version_char(key)
            if vc is None:
                logger.warning(
                    "validate_inchikey: key %s has invalid version char "
                    "(strict mode)",
                    _sanitize_string_local(key),
                )
                return False
        return True
    # [DQ-1, ARCH-1] Synthetic keys (startswith SYNTH).
    if _SYNTHETIC_INCHIKEY_PATTERN.match(key):
        return True
    # [SCI-15] Mixture keys.
    if _MIXTURE_INCHIKEY_PATTERN.match(key):
        return True
    return False


def is_valid_inchikey(key: str) -> bool:
    """Return True iff ``key`` matches the platform's InChIKey contract (ARCH-1).

    This is the SINGLE CANONICAL validator for the platform. The DB layer
    (``database.models._validate_inchikey``) and the loader fallback
    (``database.loaders._inchikey_valid``) BOTH delegate to this function
    so there is exactly one definition of "valid InChIKey" across the
    codebase.

    P1-ER-2 ROOT FIX (audit Chain 3): the v22 fix unified the validators
    by accepting TEST/OUTER/INNER/IK prefixes everywhere. That solved the
    divergent-validators compound chain at the cost of letting test
    fixtures flow through to the DB → KG. Real InChIKeys NEVER start with
    these prefixes — they were developer conveniences that leaked past
    the DB boundary. The SYNTH prefix is still accepted because it is the
    platform's own synthetic-key namespace (legitimately produced by
    ``drug_resolver.synthesize_inchikey`` for records that lack a real
    InChIKey). TEST/OUTER/INNER/IK are NOT — they were test-fixture
    shortcuts and must never appear in production data.

    Parameters
    ----------
    key : str
        The InChIKey to validate.

    Returns
    -------
    bool
        True if valid (27-char standard with optional protonation suffix,
        SYNTH-prefixed synthetic key, OR a mixture key).

    Examples
    --------
    >>> is_valid_inchikey("BSYNRYMUTXBXSQ-UHFFFAOYSA-N")
    True
    >>> is_valid_inchikey("SYNTH-001")
    True
    >>> is_valid_inchikey("IK001")
    False
    >>> is_valid_inchikey("TEST-IK-001")
    False
    >>> is_valid_inchikey("invalid")
    False
    """
    if not isinstance(key, str) or not key:
        return False
    # Normalize first (strip + upper) so the validator is forgiving.
    cleaned = key.strip().upper()
    if not cleaned:
        return False
    if _INCHIKEY_PATTERN.match(cleaned):
        return True
    if cleaned.startswith("SYNTH") and len(cleaned) >= 7:
        # SYNTH is the platform's own synthetic-key namespace (produced
        # by drug_resolver.synthesize_inchikey) — legitimately accepted.
        # P1-069 ROOT FIX: reject bare "SYNTH" (5 chars) — a valid
        # synthetic key must have at least 7 chars ("SYNTH" + at least
        # 2 chars of hash suffix). A bare "SYNTH" is almost certainly
        # a placeholder or truncated value, not a legitimate key.
        return True
    if _MIXTURE_INCHIKEY_PATTERN.match(cleaned):
        return True
    # P1-ER-2 ROOT FIX: TEST / OUTER / INNER / IK prefixes are NOT
    # accepted. They were developer conveniences for test fixtures and
    # must never flow through to the DB / KG. Reject them here so the
    # canonical contract is "real InChIKeys only, plus our own SYNTH
    # namespace, plus mixtures".
    return False


def is_synthetic_inchikey(inchikey: str) -> bool:
    """Return True iff ``inchikey`` is a SYNTH-prefixed synthetic key (COMP-2).

    Mirrors :func:`entity_resolution.drug_resolver.is_synthetic_inchikey`
    exactly — case-insensitive ``startswith("SYNTH")``.

    Parameters
    ----------
    inchikey : str
        The InChIKey to test.

    Returns
    -------
    bool
        True if the key starts with "SYNTH" (case-insensitive).
    """
    return bool(inchikey and isinstance(inchikey, str) and inchikey.upper().startswith("SYNTH"))


def standardize_inchikey(raw_inchikey: Union[str, bytes, None]) -> str | None:
    """Validate and return a cleaned InChIKey string.

    Performs three steps:
    1. :func:`normalize_inchikey` — strip whitespace, uppercase.
    2. :func:`validate_inchikey` — check format.
    3. Recovery (REL-8) — fix hyphen positions / version char if possible.

    [IDEM-7] increments module-level _normalize_call_count on every call.

    Accepts standard 27-char keys, non-standard keys (ending in N),
    mixture keys, and SYNTH-prefixed synthetic keys.

    Parameters
    ----------
    raw_inchikey : str, bytes, or None
        A raw InChIKey string, possibly with surrounding whitespace,
        wrong case, or hyphens in wrong positions.

    Returns
    -------
    str or None
        The cleaned InChIKey if valid, otherwise ``None``.

    Examples
    --------
    >>> standardize_inchikey("  BSYNRYMUTXBXSQ-UHFFFAOYSA-N  ")
    'BSYNRYMUTXBXSQ-UHFFFAOYSA-N'
    >>> standardize_inchikey("SYNTH-001")
    'SYNTH-001'
    >>> standardize_inchikey("invalid-key") is None
    True

    Notes
    -----
    The returned string is always a new object (DESIGN-11) — callers may
    freely mutate it without affecting the input.
    """
    global _normalize_call_count  # [IDEM-7] [ARCH-5] incremented below

    # [IDEM-7] Fast path: input is already normalized, canonical (27-char
    # or SYNTH/mixture), and valid. v36 ROOT FIX (Chain 2): the fast path
    # must ALSO reject suffixed keys (e.g. ``-X`` protonation) — otherwise
    # the suffixed key is returned as-is and the suffix leaks downstream.
    # We use ``is_canonical_inchikey`` (strict 27-char or SYNTH/mixture)
    # rather than the loose ``validate_inchikey``.
    #
    # P2-6 ROOT FIX (v82): the previous check
    # ``raw_inchikey == raw_inchikey.strip().upper()`` allocated TWO new
    # string objects per call (``.strip()`` and ``.upper()``) on the hot
    # path — for a 1M-row DataFrame, that's 2M allocations just to detect
    # already-normalized keys. The optimized check below uses:
    #   * ``raw_inchikey[0].isspace()`` / ``raw_inchikey[-1].isspace()``
    #     — O(1) char checks, no allocation.
    #   * ``raw_inchikey.isupper()`` — C-level scan, no allocation
    #     (returns True iff all cased chars are uppercase AND there's at
    #     least one cased char; for an InChIKey like
    #     "BSYNRYMUTXBXSQ-UHFFFAOYSA-N" this is True; for the lowercase
    #     or mixed-case variant it's False).
    # This is mathematically equivalent to the original check (no leading/
    # trailing whitespace AND all uppercase) but avoids the allocations.
    if (
        isinstance(raw_inchikey, str)
        and raw_inchikey
        and not raw_inchikey[0].isspace()
        and not raw_inchikey[-1].isspace()
        and raw_inchikey.isupper()
        and is_canonical_inchikey(raw_inchikey)
    ):
        logger.debug("standardize_inchikey: fast-path (input already standardized)")
        # Return a new string object (DESIGN-11).
        with _NORMALIZE_LOCK:
            _normalize_call_count += 1
        with _METRICS_LOCK:
            _METRICS["inchikeys_validated"] += 1
        return str(raw_inchikey)

    # [ARCH-6] Step 1: normalize.
    cleaned = normalize_inchikey(raw_inchikey)
    if cleaned is None:
        return None

    with _NORMALIZE_LOCK:
        _normalize_call_count += 1

    # v36 ROOT FIX (Chain 2): strip any non-canonical extension BEFORE
    # validation. The previous code defined ``_INCHIKEY_PATTERN`` as
    # loose (28+ chars with ``-X`` suffix) so ``validate_inchikey``
    # ACCEPTED suffixed keys, then ``standardize_inchikey`` returned
    # them unchanged. Downstream ``deduplicator._is_valid_inchikey_format``
    # (strict 27-char) silently dead-lettered them, and the DB loader
    # raised ValueError. The fix: strip the suffix here so every
    # downstream consumer sees a canonical 27-char key. This is the
    # single source of canonicalisation — no other module needs to
    # strip.
    if not is_synthetic_inchikey(cleaned):
        cleaned = strip_inchikey_extension(cleaned)

    # [ARCH-6] Step 2: validate.
    if validate_inchikey(cleaned):
        # [SCI-8] Warn on non-S/N version char (but still accept — DB compat).
        vc = _validate_inchikey_version_char(cleaned)
        if vc is None and not is_synthetic_inchikey(cleaned) and not _MIXTURE_INCHIKEY_PATTERN.match(cleaned):
            logger.warning(
                "standardize_inchikey: key %s has version char %s "
                "(not S or N) — accepted for DB-layer compatibility",
                _sanitize_string_local(cleaned),
                cleaned[-1],
            )
        # [DQ-1] DEBUG when a SYNTH key is accepted.
        if is_synthetic_inchikey(cleaned):
            logger.debug(
                "standardize_inchikey: accepted synthetic key %s",
                _sanitize_string_local(cleaned),
            )
            with _DQ_LOCK:
                _DQ_COUNTS["synthetic_keys"] += 1
        # [SCI-15] INFO when a mixture key is accepted.
        elif _MIXTURE_INCHIKEY_PATTERN.match(cleaned):
            logger.info(
                "standardize_inchikey: accepted mixture InChIKey %s",
                _sanitize_string_local(cleaned),
            )
        with _METRICS_LOCK:
            _METRICS["inchikeys_validated"] += 1
        # [LINEAGE-13] Audit log.
        _audit_log_local(
            "standardize_inchikey",
            {"input_hash": _smiles_hash(str(raw_inchikey)), "output": cleaned},
        )
        # Return a new string object (DESIGN-11).
        return str(cleaned)

    # [CODE-24] Recovery: try fixing hyphen positions.
    # NOTE: an InChIKey is 27 chars WITH hyphens (14 + 1 + 10 + 1 + 1);
    # without hyphens it's 25 chars.
    no_hyphens = cleaned.replace("-", "")
    if len(no_hyphens) == 25:
        recovered = f"{no_hyphens[:14]}-{no_hyphens[14:24]}-{no_hyphens[24]}"
        if _INCHIKEY_PATTERN.match(recovered):
            logger.info(
                "standardize_inchikey: recovered InChIKey by re-inserting "
                "hyphens: %s → %s",
                _sanitize_string_local(cleaned),
                recovered,
            )
            with _METRICS_LOCK:
                _METRICS["inchikeys_validated"] += 1
            return str(recovered)

    # [REL-8] Recovery: do NOT silently rewrite the version char
    # (the FINAL character of the InChIKey). Per the IUPAC InChIKey
    # specification (https://www.inchi-trust.org/technical-faq/), the
    # 27-char InChIKey is structured as:
    #   - chars 0-13: hash of molecular skeleton (14 uppercase letters)
    #   - char 14: hyphen
    #   - chars 15-24: hash of proton layer (10 uppercase letters)
    #   - char 25: hyphen
    #   - char 26 (FINAL char): VERSION FLAG — 'S' = standard, 'N' = non-standard.
    #
    # v29 ROOT FIX (audit C-4): the previous code mislabeled this final
    # char as the "protonation char" and only accepted N/P/M/S. This is
    # SCIENTIFICALLY WRONG — the version flag is 'S' or 'N' per IUPAC,
    # and the InChI Trust reserves the right to add new version flags
    # (B/F/T/etc.) for forward compatibility. Rejecting valid keys
    # ending in those letters would silently dead-letter legitimate
    # compounds when InChI v2+ ships.
    #
    # The CORRECT validation: the final char must be an UPPERCASE LETTER
    # (any). The strict variants S/N are checked separately by
    # _STANDARD_INCHIKEY_PATTERN / _NONSTANDARD_INCHIKEY_PATTERN for
    # callers that need to distinguish standard vs non-standard.
    if len(cleaned) == 27 and cleaned[14] == "-" and cleaned[25] == "-":
        last_char = cleaned[-1]
        if not last_char.isalpha() or not last_char.isupper():
            logger.warning(
                "standardize_inchikey: InChIKey %s has unexpected "
                "version char %s (must be uppercase letter) — "
                "refusing to rewrite; sending to dead-letter",
                _sanitize_string_local(cleaned),
                last_char,
            )
            with _METRICS_LOCK:
                _METRICS["inchikeys_rejected"] = (
                    _METRICS.get("inchikeys_rejected", 0) + 1
                )
            _add_dead_letter({
                "inchikey": _sanitize_string_local(cleaned),
                "error": "invalid_version_char",
                "error_category": "INVALID_INCHIKEY",
                "timestamp": datetime.now(timezone.utc).isoformat(),
            })
            return None

    # [SEC-9] Sanitize the cleaned string before logging (log injection).
    sanitized_for_log = _sanitize_string_local(cleaned)
    logger.warning(
        "standardize_inchikey: InChIKey '%s' does not match expected "
        "pattern [A-Z]{14}-[A-Z]{10}-[A-Z] or synthetic SYNTH pattern",
        sanitized_for_log,
    )
    with _METRICS_LOCK:
        _METRICS["inchikeys_rejected"] += 1
    # [REL-4] Dead-letter.
    _add_dead_letter({
        "inchikey": cleaned,
        "error": "invalid_inchikey_format",
        "error_category": "INVALID_INCHIKEY",
        "timestamp": datetime.now(timezone.utc).isoformat(),
    })
    # [LINEAGE-13] Audit log even for rejections.
    _audit_log_local(
        "standardize_inchikey_rejected",
        {"input": sanitized_for_log},
    )
    return None


