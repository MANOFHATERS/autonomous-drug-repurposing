class ActivityValue(tuple):
    """Normalized activity value with full provenance (DESIGN-8, ARCH-8).

    A subclass of ``tuple`` whose FIRST TWO elements are ``(value, unit)``
    so that 2-tuple unpacking continues to work for backward compatibility::

        val, unit = normalize_activity_value(1.5, "uM")  # backward compat
        result = normalize_activity_value(1.5, "uM")
        result.value          # 1500.0
        result.unit           # 'nM'
        result.censored       # False

    The extra metadata (``original_value``, ``censored``, ``warnings``,
    etc.) are stored as instance attributes (NOT as additional tuple
    elements) so unpacking with ``a, b = result`` always yields exactly
    two values.

    Attributes
    ----------
    value : float or None
        The normalized value in nM, or None if conversion failed.
    unit : str
        The output unit (always "nM" on successful conversion; otherwise
        the original unit string).
    original_value : Any
        The original input value (for audit; never modified).
    original_unit : str or None
        The original input unit.
    conversion_factor : float or None
        The multiplier applied (e.g., 1e3 for uM → nM).
    censored : bool
        True if the value was censured (">", "<", "~", or out-of-range).
    censor_direction : str or None
        One of "<", ">", "~", or None.
    activity_type : str or None
        E.g., "IC50", "Ki", "Kd", "EC50".
    temperature_c : float or None
        Temperature in Celsius (relevant for Ki; SCI-20).
    warnings : tuple[str, ...]
        Warnings generated during normalization.
    is_corrupt : bool
        v16 ROOT FIX (SW-6): True if the value is corrupt (e.g.
        negative concentration). Distinct from ``censored`` which
        means "we know it's >X or <X". Corrupt values should be
        excluded from BOTH the valid-set AND the censored-set.
    """

    # V18 ROOT FIX (SW-5 — patient safety): the v16/v17 design used a
    # module-level side-channel dict ``_AV_EXTRAS`` keyed by ``id(self)``.
    # The audit flagged that id() values can be reused after GC (a new
    # ActivityValue constructed at a recycled address could inherit a
    # dead object's extras). v16/v17 attempted to mitigate this with
    # __del__ cleanup and a defensive __init__ overwrite — but the
    # design flaw remained: a property read between __del__ and __new__
    # could return the dead object's data.
    #
    # The ROOT FIX is to eliminate the side-channel entirely: store
    # every extra field as a real instance attribute on ``self.__dict__``.
    # Since ``__slots__ = ()`` was already removed in v16, the object
    # has a __dict__; we just weren't using it. Storing on __dict__:
    #   1. Cannot be inherited from a recycled address (each instance
    #      starts with a fresh __dict__).
    #   2. Is cleaned up automatically by Python's GC (no __del__
    #      needed, no manual pop).
    #   3. Is faster (no dict lookup + id() call per property access).
    #   4. Preserves tuple-subclass semantics (still hashes by
    #      (value, unit, censored, is_corrupt) via __hash__).
    #
    # P2-1 ROOT FIX (v82): the legacy ``_AV_EXTRAS`` dict was kept as
    # a "backward-compat shim" but was NEVER populated or queried by
    # any internal caller — pure dead code. It has been REMOVED.
    # External code that still imports ``_AV_EXTRAS`` from this module
    # gets an immutable empty dict (MappingProxyType) via the module-
    # level ``__getattr__`` hook at the end of this file, plus a
    # DeprecationWarning so operators notice and migrate.

    def __new__(
        cls,
        value: float | None,
        unit: str,
        *,
        original_value: Any = None,
        original_unit: str | None = None,
        conversion_factor: float | None = None,
        censored: bool = False,
        censor_direction: str | None = None,
        activity_type: str | None = None,
        temperature_c: float | None = None,
        warnings: tuple[str, ...] = (),
        is_corrupt: bool = False,
    ) -> "ActivityValue":
        # [DESIGN-8] Store ONLY (value, unit) as tuple elements — keeps
        # 2-tuple unpacking backward-compatible.  All other fields are
        # instance attributes set on __dict__.
        self = super().__new__(cls, (value, unit))
        # V18 SW-5: direct __dict__ assignment — no side-channel dict,
        # no id(self), no __del__, no recycling race.
        self.__dict__.update({
            "original_value": original_value,
            "original_unit": original_unit,
            "conversion_factor": conversion_factor,
            "censored": censored,
            "censor_direction": censor_direction,
            "activity_type": activity_type,
            "temperature_c": temperature_c,
            "warnings": tuple(warnings),
            "is_corrupt": bool(is_corrupt),
        })
        return self

    # V18 SW-5: no __init__ override needed — __new__ sets all fields,
    # and tuple.__init__ ignores kwargs. The defensive __init__ overwrite
    # is no longer needed because there is no side-channel dict to
    # protect against stale entries.

    # V18 SW-5: no __del__ needed — instance __dict__ is cleaned up by
    # Python's GC automatically; no manual pop from a side-channel dict.

    @property
    def value(self) -> float | None:
        """The normalized value in nM (or None on failure)."""
        return self[0]

    @property
    def unit(self) -> str:
        """The output unit string."""
        return self[1]

    @property
    def original_value(self) -> Any:
        return self.__dict__.get("original_value")

    @property
    def original_unit(self) -> str | None:
        return self.__dict__.get("original_unit")

    @property
    def conversion_factor(self) -> float | None:
        return self.__dict__.get("conversion_factor")

    @property
    def censored(self) -> bool:
        return self.__dict__.get("censored", False)

    @property
    def censor_direction(self) -> str | None:
        return self.__dict__.get("censor_direction")

    @property
    def activity_type(self) -> str | None:
        return self.__dict__.get("activity_type")

    @property
    def temperature_c(self) -> float | None:
        return self.__dict__.get("temperature_c")

    @property
    def warnings(self) -> tuple[str, ...]:
        return self.__dict__.get("warnings", ())

    @property
    def is_corrupt(self) -> bool:
        """v16 SW-6: True if the value is corrupt (e.g. negative concentration)."""
        return self.__dict__.get("is_corrupt", False)

    def __repr__(self) -> str:
        return (
            f"ActivityValue(value={self.value!r}, unit={self.unit!r}, "
            f"censored={self.censored!r}, censor_direction={self.censor_direction!r}, "
            f"activity_type={self.activity_type!r}, is_corrupt={self.is_corrupt!r})"
        )

    def __eq__(self, other: object) -> bool:
        if isinstance(other, ActivityValue):
            return (
                tuple(self) == tuple(other)
                and self.original_value == other.original_value
                and self.censored == other.censored
                and self.censor_direction == other.censor_direction
                and self.is_corrupt == other.is_corrupt
            )
        if isinstance(other, tuple):
            return tuple(self) == other
        return NotImplemented

    def __hash__(self) -> int:
        return hash((self.value, self.unit, self.censored, self.is_corrupt))


