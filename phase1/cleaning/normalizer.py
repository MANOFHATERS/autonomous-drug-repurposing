def _shallow_copy_record(record: dict) -> dict:
    """Shallow-copy a record dict, copying nested mutables one level deep.

    Replaces copy.deepcopy (ARCH-10, PERF-1, IDEM-11, SEC-12, REL-5).
    Does NOT invoke __deepcopy__ (SEC-12).  Preserves insertion order
    (IDEM-11).  Only lists, dicts, and sets are copied one level deep;
    nested-nested mutables are NOT deep-copied (documented limitation).

    Parameters
    ----------
    record : dict
        The record to copy.

    Returns
    -------
    dict
        A new dict with the same keys, with mutable values shallow-copied.
    """
    out: dict[str, Any] = {}
    for key, value in record.items():
        if isinstance(value, list):
            out[key] = list(value)
        elif isinstance(value, dict):
            out[key] = dict(value)
        elif isinstance(value, set):
            out[key] = set(value)
        else:
            out[key] = value
    return out


def _round_sig(x: float, sig: int = _ACTIVITY_VALUE_SIG_FIGS) -> float:
    """Round to N significant figures (IDEM-12).

    Parameters
    ----------
    x : float
        The value to round.
    sig : int
        Number of significant figures.  Default: 6.

    Returns
    -------
    float
        The rounded value.
    """
    if not math.isfinite(x) or x == 0:
        return x
    try:
        return round(x, -int(math.floor(math.log10(abs(x)))) + (sig - 1))
    except (ValueError, OverflowError):
        return x


