def normalize_activity_value(
    value: Any,
    units: Any,
    *,
    activity_type: str | None = None,
    temperature_c: float | None = None,
    raise_on_error: bool = False,
) -> ActivityValue:
    """Convert an activity measurement to nanomolar (nM) units.

    Returns an :class:`ActivityValue` NamedTuple.  Backward-compatible
    with the v2.0.0 2-tuple return contract::

        val, unit = normalize_activity_value(1.5, "uM")
        # val == 1500.0, unit == "nM"

    Supported unit conversions (case-insensitive — DESIGN-5):

    ===========  ===========  ===========
    Input        Multiplier   Output unit
    ===========  ===========  ===========
    ``pM``       × 1e-3       ``nM``
    ``nM``       × 1.0        ``nM``
    ``uM``       × 1e3        ``nM``
    ``µM``       × 1e3        ``nM``
    ``mM``       × 1e6        ``nM``
    ``M``        × 1e9        ``nM``
    ``mol/L``    × 1e9        ``nM``
    ``%``        None         original (not convertible)
    ===========  ===========  ===========

    Censor operators (">100", "<1", "~50") are detected and the
    resulting :class:`ActivityValue` has ``censored=True`` and
    ``censor_direction`` set (SCI-10).

    Parameters
    ----------
    value : int, float, str, or None
        The numeric activity measurement.  Strings with censor prefixes
        (">", "<", "~") are parsed.
    units : str or None
        The unit string.  None / non-string logs a WARNING (DQ-13).
    activity_type : str, optional
        One of :data:`_ALLOWED_ACTIVITY_TYPES` (IC50, Ki, Kd, EC50, ...).
        If omitted, a WARNING is logged (SCI-11) because comparing across
        activity types without context is scientifically wrong.
    temperature_c : float, optional
        Temperature in Celsius (relevant for Ki; SCI-20).  Validated
        against [0, 100].
    raise_on_error : bool
        If True, raise on invalid input.  If False (default), return an
        ActivityValue with ``value=None``.

    Returns
    -------
    ActivityValue
        NamedTuple with ``value``, ``unit``, ``original_value``,
        ``original_unit``, ``conversion_factor``, ``censored``,
        ``censor_direction``, ``activity_type``, ``temperature_c``,
        ``warnings``.

    Examples
    --------
    >>> r = normalize_activity_value(1.5, "uM")
    >>> r.value, r.unit
    (1500.0, 'nM')
    >>> val, unit = r  # backward-compat 2-tuple unpacking
    >>> val, unit
    (1500.0, 'nM')
    >>> r = normalize_activity_value(">100", "uM")
    >>> r.value, r.censored, r.censor_direction
    (100000.0, True, '>')
    """
    warnings_tuple: list[str] = []
    original_value = value
    original_unit = units if isinstance(units, str) else None

    # [SCI-11] Activity type validation.
    # v16 ROOT FIX (SW-4): if the upstream caller passes the STRING
    # "None" (a common pandas/CSV artifact when a missing value is
    # stringified), coerce it to Python None so it is treated as
    # "missing" rather than as a (now-invalid) activity type.
    if isinstance(activity_type, str) and activity_type.strip() == "None":
        logger.warning(
            "normalize_activity_value: activity_type was the string 'None' "
            "— coercing to Python None (missing). See SW-4 root fix."
        )
        warnings_tuple.append("activity_type_string_none_coerced")
        activity_type = None

    if activity_type is not None:
        if activity_type not in _ALLOWED_ACTIVITY_TYPES:
            logger.warning(
                "normalize_activity_value: activity_type %r not in allowed "
                "set %s — preserving but flagging",
                activity_type,
                sorted(_ALLOWED_ACTIVITY_TYPES),
            )
            warnings_tuple.append(f"unknown_activity_type:{activity_type}")
    else:
        # [SCI-11] Warn when activity_type is omitted.
        logger.warning(
            "normalize_activity_value: activity_type not provided — "
            "downstream consumers MUST segment by activity_type before "
            "ranking (IC50 ≠ Ki ≠ Kd)"
        )
        warnings_tuple.append("missing_activity_type")

    # [SCI-20] Temperature validation.
    if temperature_c is not None:
        try:
            temperature_c = float(temperature_c)
            if not (0.0 <= temperature_c <= 100.0):
                logger.warning(
                    "normalize_activity_value: temperature_c %s out of "
                    "range [0, 100] — preserving but flagging",
                    temperature_c,
                )
                warnings_tuple.append(f"temperature_out_of_range:{temperature_c}")
        except (ValueError, TypeError):
            logger.warning(
                "normalize_activity_value: temperature_c %r is not numeric "
                "— ignoring",
                temperature_c,
            )
            temperature_c = None

    # [DQ-13] Handle None / non-string units.
    if units is None:
        logger.warning(
            "normalize_activity_value: units is None — caller may have "
            "intended 'nM' (returning value=None, unit='')"
        )
        warnings_tuple.append("units_is_none")
        return ActivityValue(
            value=None,
            unit="",
            original_value=original_value,
            original_unit=original_unit,
            censored=False,
            activity_type=activity_type,
            temperature_c=temperature_c,
            warnings=tuple(warnings_tuple),
        )
    if not isinstance(units, str):
        # P1-033 ROOT FIX (NaN units coerced to "nan" string):
        #   The previous code did ``units = str(units)`` for any non-str,
        #   non-None value. For ``float('nan')``, ``np.nan``, or pandas
        #   ``pd.NA`` / ``pd.NaT`` this produces the strings ``"nan"`` or
        #   ``"<NA>"`` — which then fail the unit lookup at line ~4545
        #   (``_UNIT_CONVERSIONS_CF.get("nan".casefold())`` returns None)
        #   and the function returns ``value=numeric_value`` unchanged
        #   with ``unit="nan"`` — silently passing the original value as
        #   if it were already in nM. Same impact as P1-014 (wrong
        #   magnitude). The ``unit="nan"`` string in the DB CHECK
        #   constraint may or may not pass depending on the constraint.
        #
        #   ROOT FIX: explicitly check for NaN/NA values WITHOUT
        #   requiring pandas (normalizer.py is imported lazily and pd
        #   may not be in scope at this point). Use ``math.isnan`` for
        #   floats, ``is None`` for None, and a defensive string check
        #   for pandas NA sentinels (``str(pd.NA) == "<NA>"``). For
        #   NaN/NA we log a warning and return ``value=None`` with
        #   ``unit=""`` — the same behavior as the ``units is None``
        #   branch above. This prevents the silent "nan"-string
        #   passthrough.
        import math as _math
        _is_na = False
        if units is None:
            _is_na = True  # defensive — the early-return above should have caught this
        elif isinstance(units, float) and _math.isnan(units):
            _is_na = True
        elif isinstance(units, str) and units.lower() in ("nan", "<na>", "na"):
            # Already a string NaN/NA sentinel (e.g. from a previous
            # str() coercion upstream) — treat as missing.
            _is_na = True
        else:
            # Catch pandas NA sentinels (pd.NA, pd.NaT) without importing
            # pandas. ``str(pd.NA) == "<NA>"``, ``str(pd.NaT) == "NaT"``.
            try:
                _s = str(units)
                if _s in ("<NA>", "NaT", "<NA>", "nan", "NaN"):
                    _is_na = True
            except Exception:
                pass
        if _is_na:
            logger.warning(
                "normalize_activity_value: units is NaN/NA (type=%s, "
                "value=%r) — treating as missing (returning value=None, "
                "unit='')",
                type(units).__name__, units,
            )
            warnings_tuple.append("units_is_nan")
            return ActivityValue(
                value=None,
                unit="",
                original_value=original_value,
                original_unit=original_unit,
                censored=False,
                activity_type=activity_type,
                temperature_c=temperature_c,
                warnings=tuple(warnings_tuple),
            )
        logger.debug(
            "normalize_activity_value: units is %s, not str — coercing",
            type(units).__name__,
        )
        try:
            units = str(units)
        except Exception:
            units = ""
    if units == "":
        # v36 ROOT FIX (Phase 1 Issue #34): if activity_type is p-scale
        # (pKi/pIC50/pEC50/pKd), the value is a LOG10 concentration,
        # NOT a raw concentration needing unit conversion. We must NOT
        # early-return on empty units for p-scale types — fall through
        # to the p-scale conversion block below.
        _is_p_scale = (
            activity_type is not None
            and isinstance(activity_type, str)
            and activity_type.strip() in ("pKi", "pIC50", "pEC50", "pKd", "pKb", "pED50", "pAC50")
        )
        if not _is_p_scale:
            logger.debug(
                "normalize_activity_value: empty units — returning value=None"
            )
            warnings_tuple.append("units_empty")
            return ActivityValue(
                value=None,
                unit="",
                original_value=original_value,
                original_unit=original_unit,
                censored=False,
                activity_type=activity_type,
                temperature_c=temperature_c,
                warnings=tuple(warnings_tuple),
            )
        # p-scale: fall through to conversion (units not needed).
        logger.debug(
            "normalize_activity_value: empty units but activity_type=%s "
            "is p-scale — proceeding with p-scale conversion",
            activity_type,
        )

    # Handle None value.
    if value is None:
        return ActivityValue(
            value=None,
            unit=units,
            original_value=original_value,
            original_unit=original_unit,
            censored=False,
            activity_type=activity_type,
            temperature_c=temperature_c,
            warnings=tuple(warnings_tuple),
        )

    # [SCI-10] Censor prefix detection.
    censored = False
    censor_direction: str | None = None
    numeric_value: float | None = None

    if isinstance(value, str):
        m = _CENSOR_PATTERN.match(value)
        if m:
            censor_direction = m.group(1)
            try:
                numeric_value = float(m.group(2))
                censored = True
            except (ValueError, TypeError):
                numeric_value = None
        else:
            try:
                numeric_value = float(value)
            except (ValueError, TypeError):
                # [DESIGN-8] Return value=None on parse failure.
                logger.warning(
                    "normalize_activity_value: non-numeric value %r with "
                    "units %r",
                    value,
                    units,
                )
                warnings_tuple.append("non_numeric_value")
                return ActivityValue(
                    value=None,
                    unit=units,
                    original_value=original_value,
                    original_unit=original_unit,
                    censored=False,
                    activity_type=activity_type,
                    temperature_c=temperature_c,
                    warnings=tuple(warnings_tuple),
                )
    else:
        # [REL-15] Guard against datetime / NaT / non-numeric types.
        try:
            # [CODE-3] bool is not a valid activity value.
            if isinstance(value, bool):
                logger.warning(
                    "normalize_activity_value: bool value %r — treating as None",
                    value,
                )
                warnings_tuple.append("bool_value")
                return ActivityValue(
                    value=None,
                    unit=units,
                    original_value=original_value,
                    original_unit=original_unit,
                    censored=False,
                    activity_type=activity_type,
                    temperature_c=temperature_c,
                    warnings=tuple(warnings_tuple),
                )
            numeric_value = float(value)
        except (ValueError, TypeError):
            # [DESIGN-8, REL-15] Return value=None on parse failure.
            logger.warning(
                "normalize_activity_value: non-numeric value %r (type %s) "
                "with units %r",
                value,
                type(value).__name__,
                units,
            )
            warnings_tuple.append("non_numeric_value")
            return ActivityValue(
                value=None,
                unit=units,
                original_value=original_value,
                original_unit=original_unit,
                censored=False,
                activity_type=activity_type,
                temperature_c=temperature_c,
                warnings=tuple(warnings_tuple),
            )

    if numeric_value is None:
        return ActivityValue(
            value=None,
            unit=units,
            original_value=original_value,
            original_unit=original_unit,
            censored=censored,
            censor_direction=censor_direction,
            activity_type=activity_type,
            temperature_c=temperature_c,
            warnings=tuple(warnings_tuple),
        )

    # [REL-9] Infinity / NaN guard.
    if not math.isfinite(numeric_value):
        logger.warning(
            "normalize_activity_value: value %r is not finite (inf/NaN) — "
            "returning value=None",
            numeric_value,
        )
        warnings_tuple.append("non_finite_value")
        return ActivityValue(
            value=None,
            unit=units,
            original_value=original_value,
            original_unit=original_unit,
            censored=censored,
            censor_direction=censor_direction,
            activity_type=activity_type,
            temperature_c=temperature_c,
            warnings=tuple(warnings_tuple),
        )

    # [SCI-9] Negative concentration guard.
    # v16 ROOT FIX (SW-6): the previous code returned ``censored=True,
    # censor_direction=None`` for negative values. "Censored" in
    # pharmacology means "we know the true value is >X or <X but
    # couldn't measure precisely" — e.g. ``">30 nM"`` or ``"<0.1 nM"``.
    # A NEGATIVE concentration is not censored, it is CORRUPT —
    # concentrations cannot be negative in any unit system.
    # Marking it censored caused downstream TransE training to
    # include corrupt negatives in the "censored but valid" filter,
    # biasing the model toward impossibly-potent edges.
    # The fix: mark as ``is_corrupt=True`` (new flag) and
    # ``censored=False`` so corrupt values are excluded from BOTH
    # the "valid measurement" set AND the "censored measurement" set.
    if numeric_value < 0:
        logger.warning(
            "normalize_activity_value: negative value %r — returning "
            "value=None, is_corrupt=True (impossible concentration, "
            "NOT censored — see SW-6 root fix)",
            numeric_value,
        )
        warnings_tuple.append("negative_value_corrupt")
        return ActivityValue(
            value=None,
            unit=units,
            original_value=original_value,
            original_unit=original_unit,
            censored=False,
            censor_direction=None,
            activity_type=activity_type,
            temperature_c=temperature_c,
            warnings=tuple(warnings_tuple),
            is_corrupt=True,
        )

    # [DQ-13] Log INFO when value == 0 (suspicious but not impossible).
    if numeric_value == 0:
        logger.info(
            "normalize_activity_value: value is 0 — suspicious but may be "
            "legitimate ('no activity')"
        )

    # v36 ROOT FIX (Phase 1 Issue #34 — SCIENTIFIC): p-scale activity
    # types (pKi, pIC50, pEC50, pKd) are LOG10 values, not nM
    # concentrations. The previous code accepted them as valid activity
    # types (in _ALLOWED_ACTIVITY_TYPES) but normalized_activity_value
    # treated them as raw numbers — a pIC50 of 6.0 (meaning 1 µM = 1000
    # nM) was passed through unchanged as the value 6.0, mixing with
    # nM values (e.g. 1000.0) in the same activity_value column. Any
    # downstream ML model that treats them as comparable was silently
    # corrupted.
    #
    # The fix: convert p-scale values to nM BEFORE the unit lookup.
    # The conversion is: nM = 10^(9 - pValue). E.g. pIC50=6.0 →
    # 10^3 = 1000 nM (which is 1 µM, the standard interpretation).
    # This matches the ChEMBL documentation for pChEMBL values.
    #
    # v80 FORENSIC ROOT FIX (P0-D4 — p-scale conversion DROPS censor
    #   info):
    #   The previous p-scale conversion block unconditionally set
    #   ``censored=False`` and ``censor_direction=None`` in the
    #   returned ActivityValue. This meant a censored pIC50 like ">6"
    #   (which the censor-prefix detection at line ~3916 had already
    #   flagged as ``censored=True, censor_direction=">"``) was
    #   converted to 1000 nM with ``censored=False`` — losing the
    #   censoring flag. Downstream potency ranking then treated this
    #   as a measured 1000 nM value (not an upper bound), and TransE
    #   training saw a fake precise data point, biasing the model
    #   toward high-potency predictions.
    #
    #   ROOT FIX: preserve the ``censored`` and ``censor_direction``
    #   values from the input (which were set by the censor-prefix
    #   detection block above). The p-scale conversion changes the
    #   NUMERIC value (6 → 1000 nM) but does NOT change the SEMANTIC
    #   meaning (still an upper bound if the input was ">6"). The
    #   conversion factor is None (not a simple unit multiplier) so
    #   we also preserve that.
    if activity_type is not None and isinstance(activity_type, str):
        _at = activity_type.strip()
        if _at in ("pKi", "pIC50", "pEC50", "pKd", "pKb", "pED50", "pAC50"):
            try:
                # P1-037 ROOT FIX (p-scale overflow returns before cap check):
                #   The previous code did
                #     ``converted_to_nM = float(10 ** (9.0 - float(numeric_value)))``
                #   and returned the result IMMEDIATELY (line ~4565). For a
                #   corrupted pIC50 of -10 (data entry error, or a parsing
                #   bug), the conversion produces ``10^19 nM`` — a valid
                #   Python float (no overflow) but a non-physical value.
                #   The ``abs(converted) > _ACTIVITY_CENSORED_MAX`` check
                #   at line ~4635 runs AFTER the p-scale return at line
                #   ~4565, so the 1e19 value was returned as-is. The DB
                #   CHECK ``activity_value > 0`` passed. Downstream ML saw
                #   a fake 1e19 nM value, corrupting any normalization.
                #
                #   ROOT FIX (defense-in-depth, two layers):
                #   (1) Pre-conversion range check on ``numeric_value``:
                #       p-scale values are log10 concentrations; physically
                #       plausible range is [0, 14] (1 M down to 1 fM, which
                #       covers every real biochemical measurement). Values
                #       outside this range are data entry errors — return
                #       value=None with a warning instead of producing a
                #       non-physical converted value.
                #   (2) Post-conversion cap check on ``converted_to_nM``:
                #       even for in-range p-scale values, if the converted
                #       value exceeds ``_ACTIVITY_CENSORED_MAX`` (1 mM),
                #       clip to the cap and mark as censored (same logic as
                #       the unit-conversion path at line ~4635). This
                #       guarantees p-scale-converted values are bounded
                #       by the same physical cap as unit-converted values.
                _p_scale_min: float = 0.0
                _p_scale_max: float = 14.0
                if (
                    not isinstance(numeric_value, (int, float))
                    or numeric_value < _p_scale_min
                    or numeric_value > _p_scale_max
                ):
                    logger.warning(
                        "normalize_activity_value: p-scale value %s "
                        "(activity_type=%s) outside physical range "
                        "[%g, %g] — returning value=None (likely data "
                        "entry error or parsing bug)",
                        numeric_value, _at, _p_scale_min, _p_scale_max,
                    )
                    warnings_tuple.append(
                        f"p_scale_out_of_range:{_at}:{numeric_value}"
                    )
                    return ActivityValue(
                        value=None,
                        unit="",
                        original_value=original_value,
                        original_unit=original_unit,
                        censored=False,
                        activity_type=activity_type,
                        temperature_c=temperature_c,
                        warnings=tuple(warnings_tuple),
                    )
                # p-scale → nM: 10^(9 - pValue)
                # pIC50=6 → 10^3 nM = 1 µM ✓
                # pIC50=9 → 10^0 nM = 1 nM ✓
                # pIC50=3 → 10^6 nM = 1 mM ✓ (at the cap)
                #
                # P1-020 ROOT FIX (Team-2 — pED50 dimensional ambiguity):
                #   ED50 is "Effective Dose 50" — typically a DOSE
                #   (mg/kg, in vivo) rather than a CONCENTRATION
                #   (Molar, in vitro). Applying ``10^(9 - pED50)``
                #   assumes pED50 = -log10(ED50_in_M) — i.e. ED50 is
                #   in Molar units. This is dimensionally correct for
                #   in vitro dose-response assays (where ED50 ≈ EC50
                #   and is in nM), but WRONG for in vivo assays (where
                #   ED50 is in mg/kg and the conversion produces
                #   garbage nM values).
                #   ROOT FIX (option b per P1-020 issue spec): treat
                #   pED50 as pEC50-equivalent — the pharmacology
                #   convention for dose-response assays. Add a
                #   ``ped50_assumed_ec50_equivalent`` warning tag and
                #   log at INFO so operators can see when pED50 values
                #   are processed and verify the in vitro assumption.
                #   Downstream filtering code can use this tag to
                #   exclude pED50 values from potency rankings if the
                #   in vitro assumption is unsafe for the use case.
                if _at == "pED50":
                    logger.info(
                        "normalize_activity_value: pED50 conversion "
                        "ASSUMES in vitro molar concentration (pED50 ≈ "
                        "pEC50 per pharmacology dose-response convention). "
                        "If this value is from an in vivo assay (ED50 in "
                        "mg/kg), the nM result is dimensionally invalid. "
                        "Value=%s → %.4g nM (assumed).",
                        numeric_value,
                        float(10 ** (9.0 - float(numeric_value))),
                    )
                    warnings_tuple.append("ped50_assumed_ec50_equivalent")
                converted_to_nM = float(10 ** (9.0 - float(numeric_value)))
                # P1-037 layer 2: post-conversion cap check.
                if abs(converted_to_nM) > _ACTIVITY_CENSORED_MAX:
                    logger.warning(
                        "normalize_activity_value: p-scale converted "
                        "value %g nM (activity_type=%s, value=%s) "
                        "exceeds cap %s — clipping to cap and marking "
                        "as censored",
                        converted_to_nM, _at, numeric_value,
                        _ACTIVITY_CENSORED_MAX,
                    )
                    warnings_tuple.append(
                        f"p_scale_value_exceeds_cap:{converted_to_nM}"
                    )
                    # Clip to cap (same convention as the unit-conversion
                    # path: censored values get value=threshold).
                    _clipped_value = (
                        _ACTIVITY_CENSORED_MAX
                        if converted_to_nM > 0
                        else -_ACTIVITY_CENSORED_MAX
                    )
                    # Preserve censored/censor_direction from input if
                    # already censored; default to ">" for uncensored
                    # inputs that exceed the cap post-conversion.
                    _new_censor_direction = (
                        censor_direction if censored else ">"
                    )
                    return ActivityValue(
                        value=_clipped_value,
                        unit="nM",
                        original_value=original_value,
                        original_unit=original_unit,
                        conversion_factor=None,
                        censored=True,
                        censor_direction=_new_censor_direction,
                        activity_type=activity_type,
                        temperature_c=temperature_c,
                        warnings=tuple(warnings_tuple),
                    )
                logger.debug(
                    "normalize_activity_value: p-scale conversion "
                    "(activity_type=%s, value=%s, censored=%s, "
                    "censor_direction=%s) → %.4g nM",
                    _at, numeric_value, censored, censor_direction,
                    converted_to_nM,
                )
                warnings_tuple.append(f"p_scale_converted:{_at}")
                # v80 P0-D4: preserve censored + censor_direction from
                # the input. The p-scale conversion changes the numeric
                # value but NOT the censoring semantics.
                return ActivityValue(
                    value=converted_to_nM,
                    unit="nM",
                    original_value=original_value,
                    original_unit=original_unit,
                    conversion_factor=None,  # not a simple unit multiplier
                    censored=censored,
                    censor_direction=censor_direction,
                    activity_type=activity_type,
                    temperature_c=temperature_c,
                    warnings=tuple(warnings_tuple),
                )
            except (ValueError, TypeError, OverflowError) as exc:
                logger.warning(
                    "normalize_activity_value: p-scale conversion failed "
                    "for activity_type=%s value=%s (%s) — returning value "
                    "unchanged with warning",
                    _at, numeric_value, exc,
                )
                warnings_tuple.append(f"p_scale_conversion_failed:{_at}")

    # [DESIGN-5] Case-insensitive unit lookup.
    normalized_units = units.strip()
    factor = _UNIT_CONVERSIONS_CF.get(normalized_units.casefold())

    if factor is None:
        # [DESIGN-6] Recognized but non-convertible units (e.g., "%").
        if normalized_units == "%":
            logger.info(
                "normalize_activity_value: unit '%%' is not convertible to "
                "nM — returning value unchanged"
            )
            warnings_tuple.append("percent_not_convertible")
            return ActivityValue(
                value=numeric_value,
                unit=normalized_units,
                original_value=original_value,
                original_unit=original_unit,
                censored=censored,
                censor_direction=censor_direction,
                activity_type=activity_type,
                temperature_c=temperature_c,
                warnings=tuple(warnings_tuple),
            )
        # Unknown unit — return as-is.
        logger.debug(
            "normalize_activity_value: unrecognised unit %r — returning "
            "value unchanged",
            normalized_units,
        )
        warnings_tuple.append(f"unknown_unit:{normalized_units}")
        return ActivityValue(
            value=numeric_value,
            unit=normalized_units,
            original_value=original_value,
            original_unit=original_unit,
            censored=censored,
            censor_direction=censor_direction,
            activity_type=activity_type,
            temperature_c=temperature_c,
            warnings=tuple(warnings_tuple),
        )

    # [DESIGN-6, CODE-9] Convert to nM.
    converted = numeric_value * factor

    # [DQ-7] Range validation post-conversion.
    # v66 ROOT FIX (P1C-020): use the clearly-named ``_ACTIVITY_CENSORED_MAX``
    # (1e6 = 1 mM censored threshold) instead of the misleading legacy
    # ``_ACTIVITY_VALUE_MAX`` alias. Same value, self-documenting name.
    #
    # P1-027 ROOT FIX (Team-2 — dead ``abs()`` on the cap check):
    #   The previous code was ``if abs(converted) > _ACTIVITY_CENSORED_MAX``.
    #   The ``abs()`` was DEAD CODE: ``converted = numeric_value * factor``
    #   where ``factor`` is always positive (the ``_UNIT_CONVERSIONS_CF``
    #   table maps unit strings to positive multipliers — nM=1.0,
    #   µM=1000.0, pM=0.001, etc.) and ``numeric_value`` is guarded to
    #   be non-negative at the early-return on line 4439 (negative values
    #   are flagged ``is_corrupt=True`` and never reach this point).
    #   Therefore ``converted >= 0`` always holds, and ``abs()`` is a
    #   no-op. The ``abs()`` was MISLEADING — a future maintainer who
    #   adds a negative factor to ``_UNIT_CONVERSIONS_CF`` (e.g. for an
    #   inverted unit) would believe the ``abs()`` protects them, but
    #   the early-return on line 4439 would still reject the negative
    #   INPUT before the multiplication. ROOT FIX: remove the ``abs()``
    #   and add an explicit comment documenting the invariant so future
    #   maintainers don't reintroduce the misleading guard.
    # converted is always >= 0 here:
    #   * numeric_value is guarded to be >= 0 at the early-return on
    #     line 4439 (negative values are flagged is_corrupt=True).
    #   * factor is always positive (the _UNIT_CONVERSIONS_CF table
    #     maps unit strings to positive multipliers — see definition
    #     above). If a future maintainer adds a negative factor for
    #     an inverted unit, they MUST also adjust the negative-value
    #     guard on line 4439 — otherwise the input would be rejected
    #     before the multiplication.
    if converted > _ACTIVITY_CENSORED_MAX:
        logger.warning(
            "normalize_activity_value: converted value %s exceeds cap %s "
            "— marking as censored (effectively '> measurable')",
            converted,
            _ACTIVITY_CENSORED_MAX,
        )
        warnings_tuple.append(f"value_exceeds_cap:{converted}")
        # v41 ROOT FIX (P1 #35): clip the value to the threshold so
        # ``censored=True`` values have ``value = threshold`` (not the
        # original non-physical value). This follows the pharmacology
        # convention: ">1 mM" → value=1e6 (the threshold), censored=True.
        # The previous code kept the original value (e.g. 1.5e6) which
        # was semantically muddy — was ``value`` the threshold or the
        # actual measurement? Now it's always the threshold.
        #
        # v42 ROOT FIX (P1-A-5): the v41 fix unconditionally set
        # ``censor_direction=">"``, which OVERWROTE the input's
        # ``censor_direction`` when the input was ALREADY censored
        # (``censored=True``). A ``"<2 mM"`` measurement above the 1 mM
        # cap became ``">1 mM"`` — pharmacological meaning INVERTED:
        # the drug appeared MORE potent (lower bound implied) when the
        # original measurement was an UPPER BOUND (less potent). ROOT
        # FIX: preserve the original ``censor_direction`` for inputs
        # that were already censored; only default to ``">"`` for
        # inputs that were NOT censored (i.e. uncapped raw values that
        # exceed the post-conversion cap).
        new_censor_direction = ">" if not censored else censor_direction
        return ActivityValue(
            value=_ACTIVITY_CENSORED_MAX,  # v41: clip to censored threshold (v66: renamed from _ACTIVITY_VALUE_MAX for clarity — P1C-020)
            unit="nM",
            original_value=original_value,
            original_unit=original_unit,
            conversion_factor=factor,
            censored=True,
            censor_direction=new_censor_direction,  # v42: preserve input direction
            activity_type=activity_type,
            temperature_c=temperature_c,
            warnings=tuple(warnings_tuple),
        )

    # [IDEM-12] Round to significant figures.
    converted = _round_sig(converted, _ACTIVITY_VALUE_SIG_FIGS)

    # [LOG-3] Metric.
    with _METRICS_LOCK:
        _METRICS["activity_values_normalized"] += 1

    # [LINEAGE-14] Audit log.
    _audit_log_local(
        "normalize_activity_value",
        {
            "original_value": str(original_value),
            "original_unit": original_unit,
            "converted_value": converted,
            "converted_unit": "nM",
            "factor": factor,
            "censored": censored,
            "activity_type": activity_type,
        },
    )

    return ActivityValue(
        value=converted,
        unit="nM",
        original_value=original_value,
        original_unit=original_unit,
        conversion_factor=factor,
        censored=censored,
        censor_direction=censor_direction,
        activity_type=activity_type,
        temperature_c=temperature_c,
        warnings=tuple(warnings_tuple),
    )


def normalize_activity_values(
    values: Iterable[Any],
    units: Union[Iterable[str], str],
    *,
    activity_type: str | None = None,
    temperature_c: float | None = None,
) -> list[ActivityValue]:
    """Batch activity value normalization (PERF-8).

    Parameters
    ----------
    values : iterable
        Activity values.
    units : iterable of str OR str
        If a string, broadcast to all values.  If an iterable, must be the
        same length as values.
    activity_type : str, optional
        Forwarded to :func:`normalize_activity_value`.
    temperature_c : float, optional
        Forwarded to :func:`normalize_activity_value`.

    Returns
    -------
    list[ActivityValue]
        Normalized activity values.
    """
    values_list = list(values)
    if isinstance(units, str):
        units_list = [units] * len(values_list)
    else:
        units_list = list(units)
    if len(units_list) != len(values_list):
        raise ValueError(
            f"normalize_activity_values: values ({len(values_list)}) and "
            f"units ({len(units_list)}) must have the same length"
        )
    return [
        normalize_activity_value(v, u, activity_type=activity_type, temperature_c=temperature_c)
        for v, u in zip(values_list, units_list)
    ]


