def _truncate_for_log(s: str, n: int = _LOG_SMILES_TRUNC) -> str:
    """Truncate a string for safe logging (SEC-4, CODE-8).

    Returns the first ``n`` chars plus a sha256 hash suffix for
    traceability::

        _truncate_for_log("CC(=O)OC1=CC=CC=C1C(=O)O")
        # -> "CC(=O)OC1=CC=CC=C1C(=O)O...(sha256:abc12345)"

    For strings shorter than ``n``, returns the string unchanged.

    Parameters
    ----------
    s : str
        The string to truncate.
    n : int
        Max length of the visible prefix.  Default: 30.

    Returns
    -------
    str
        Truncated + hashed string.
    """
    if not isinstance(s, str):
        s = str(s)
    if len(s) <= n:
        return s
    digest = hashlib.sha256(s.encode("utf-8", errors="replace")).hexdigest()[:8]
    return f"{s[:n]}...(sha256:{digest})"


